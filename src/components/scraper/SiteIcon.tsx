export function SiteIcon({ url, siteType, size = 'md', showBackground = false, className }: SiteIconProps) {
  // Determine the site type - prefer explicit siteType, fall back to URL detection
  const detectedType: SiteType = (siteType as SiteType) || (url ? detectSiteType(url) : 'generic');
  const config = SITE_TYPE_CONFIG[detectedType] || SITE_TYPE_CONFIG.generic;

  const iconClass = cn(sizeClasses[size], className);
  
  const renderIcon = () => {
    switch (detectedType) {
      case 'linkedin':
        return <LinkedInIcon className={iconClass} />;
      case 'reddit':
        return <RedditIcon className={iconClass} />;
      case 'youtube':
        return <YouTubeIcon className={iconClass} />;
      case 'facebook':
        return <FacebookIcon className={iconClass} />;
      case 'twitter':
        return <TwitterIcon className={iconClass} />;
      case 'instagram':
        return <InstagramIcon className={iconClass} />;
      case 'tiktok':
        return <TikTokIcon className={iconClass} />;
      case 'medium':
        return <MediumIcon className={iconClass} />;
      case 'substack':
        return <SubstackIcon className={iconClass} />;
      case 'github':
        return <Github className={iconClass} />;
      case 'hackernews':
        return <HackerNewsIcon className={iconClass} />;
      case 'producthunt':
        return <ProductHuntIcon className={iconClass} />;
      case 'wordpress':
        return <WordPressIcon className={iconClass} />;
      case 'shopify':
        return <ShopifyIcon className={iconClass} />;
      default:
        return <Globe className={iconClass} />;
    }
  };

  if (showBackground) {
    return (
      <div
        className={cn(
          'rounded-lg flex items-center justify-center',
          containerSizes[size]
        )}
        style={{ backgroundColor: config.bgColor }}
      >
        <div style={{ color: config.color }}>
          {renderIcon()}
        </div>
      </div>
    );
  }

  return (
    <div style={{ color: config.color }} title={config.label}>
      {renderIcon()}
    </div>
  );
}
